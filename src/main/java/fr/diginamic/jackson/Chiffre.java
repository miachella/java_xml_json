package fr.diginamic.jackson;

public class Chiffre {
	
	protected int annee;
	protected double montant;
	
	public int getAnnee() {
		return annee;
	}
	public double getMontant() {
		return montant;
	}
	public void setAnnee(int annee) {
		this.annee = annee;
	}
	public void setMontant(double montant) {
		this.montant = montant;
	}

}
